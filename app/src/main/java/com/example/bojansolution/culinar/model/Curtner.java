public class Curtner {
    private Long curtnerId;
    private String curtnerCode;
    private String curtnerName;
    private String curtnerStatus;

    public Long getCurtnerId() {
        return curtnerId;
    }

    public void setCurtnerId(Long curtnerId) {
        this.curtnerId = curtnerId;
    }

    public String getCurtnerCode() {
        return curtnerCode;
    }

    public void setCurtnerCode(String curtnerCode) {
        this.curtnerCode = curtnerCode;
    }

    public String getCurtnerName() {
        return curtnerName;
    }

    public void setCurtnerName(String curtnerName) {
        this.curtnerName = curtnerName;
    }

    public String getCurtnerStatus() {
        return curtnerStatus;
    }

    public void setCurtnerStatus(String curtnerStatus) {
        this.curtnerStatus = curtnerStatus;
    }
}
